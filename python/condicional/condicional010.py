numero1 = float(input('Numero 1: '))
numero2 = float(input('Numero 2: '))
escolha = input('escolha (+),(-),(*),(/): ')
if escolha == '+':
    print(numero1 + numero2)
elif escolha == '-':
    print(numero1 - numero2)
elif escolha == '*':
    print(numero1 * numero2)
elif escolha == '/':
    print(numero1 / numero2)
